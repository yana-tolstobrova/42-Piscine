#include <unistd.h>

int	main(int argc, char **argv)
{
	int	len;
	int	i;
	int	j;

	i = 1;
	len = 0;
	while (argv[len])
		len++;
	if (argc >= 2)
	{
		while (i < len)
		{
			j = 0;
			while (argv[i][j] != '\0')
			{
				write(1, &argv[i][j], 1);
				j++;
			}
			write(1, "\n", 1);
			i++;
		}
	}
}
